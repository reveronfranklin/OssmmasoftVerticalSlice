CREATE OR REPLACE PROCEDURE SP_SOP_COMM_INS (
  p_TICKET_ID IN NUMBER,
  p_USUARIO_ID IN NUMBER,
  p_COMENTARIO IN VARCHAR2,
  p_ES_INTERNO IN NUMBER,
  p_CODIGO_EMPRESA IN NUMBER,
  p_COMENTARIO_ID_OUT OUT NUMBER,
  p_Message OUT VARCHAR2
) AS
  v_solicitante NUMBER;
  v_responsable NUMBER;
BEGIN
  SELECT USUARIO_SOLICITANTE_ID, USUARIO_RESPONSABLE_ID
    INTO v_solicitante, v_responsable
    FROM SOP_TICKET
   WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  p_COMENTARIO_ID_OUT := SEQ_SOP_COMM.NEXTVAL;
  INSERT INTO SOP_TICKET_COMENTARIO
    (COMENTARIO_ID, CODIGO_EMPRESA, TICKET_ID, USUARIO_ID, COMENTARIO, ES_INTERNO)
  VALUES
    (p_COMENTARIO_ID_OUT, p_CODIGO_EMPRESA, p_TICKET_ID, p_USUARIO_ID, p_COMENTARIO, NVL(p_ES_INTERNO, 0));

  INSERT INTO SOP_TICKET_HISTORIAL
    (HISTORIAL_ID, CODIGO_EMPRESA, TICKET_ID, TIPO_CAMBIO, CAMPO, VALOR_NUEVO, COMENTARIO, USUARIO_ID)
  VALUES
    (SEQ_SOP_HIST.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, 'COMENTARIO', 'COMENTARIO_ID', TO_CHAR(p_COMENTARIO_ID_OUT), p_COMENTARIO, p_USUARIO_ID);

  INSERT INTO SOP_TICKET_NOTIF
    (NOTIF_ID, CODIGO_EMPRESA, TICKET_ID, USUARIO_DESTINO_ID, TIPO_DESTINO, EVENTO, TITULO, MENSAJE, CANAL)
  SELECT SEQ_SOP_NOTIF.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, destino.USUARIO_ID,
         destino.TIPO_DESTINO, 'COMENTADO', 'Nuevo comentario',
         'El ticket tiene un nuevo comentario.', 'INTERNA'
    FROM (
      SELECT v_solicitante USUARIO_ID, 'SOLICITANTE' TIPO_DESTINO FROM DUAL
      UNION
      SELECT v_responsable USUARIO_ID, 'RESPONSABLE' TIPO_DESTINO FROM DUAL
    ) destino
   WHERE destino.USUARIO_ID IS NOT NULL
     AND destino.USUARIO_ID <> p_USUARIO_ID
     AND NVL(p_ES_INTERNO, 0) = 0;

  INSERT INTO SIS_EMAIL_QUEUE (
    EMAIL_ID, CODIGO_EMPRESA, MODULO_ORIGEN, REFERENCIA_ID, TO_EMAIL,
    TO_NAME, SUBJECT, BODY_HTML, BODY_TEXT, FECHA_PROGRAMADA
  )
  SELECT SEQ_EMAIL_Q.NEXTVAL, p_CODIGO_EMPRESA, 'SOPORTE', p_TICKET_ID,
         u.EMAIL, u.USUARIO, 'Nuevo comentario en ticket',
         '<p>El ticket tiene un nuevo comentario.</p>',
         'El ticket tiene un nuevo comentario.',
         SYSDATE
    FROM SIS_USUARIOS u
   WHERE u.CODIGO_EMPRESA = p_CODIGO_EMPRESA
     AND u.CODIGO_USUARIO IN (v_solicitante, v_responsable)
     AND u.CODIGO_USUARIO <> p_USUARIO_ID
     AND NVL(p_ES_INTERNO, 0) = 0
     AND NVL(u.RECIBE_EMAIL, 1) = 1
     AND u.EMAIL IS NOT NULL;

  p_Message := 'Success';
EXCEPTION
  WHEN OTHERS THEN
    p_COMENTARIO_ID_OUT := 0;
    p_Message := SQLERRM;
END;
