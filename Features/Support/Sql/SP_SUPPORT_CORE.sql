CREATE OR REPLACE PROCEDURE SP_SOP_TKT_ASSIGN (
  p_TICKET_ID IN NUMBER,
  p_USUARIO_RESPONSABLE_ID IN NUMBER,
  p_UPDATED_BY IN NUMBER,
  p_COMENTARIO IN VARCHAR2,
  p_CODIGO_EMPRESA IN NUMBER,
  p_Message OUT VARCHAR2
) AS
  v_old NUMBER;
  v_analista NUMBER;
BEGIN
  SELECT COUNT(1) INTO v_analista
    FROM SIS_USUARIOS
   WHERE CODIGO_USUARIO = p_USUARIO_RESPONSABLE_ID
     AND CODIGO_EMPRESA = p_CODIGO_EMPRESA
     AND NVL(STATUS, 'A') IN ('1', 'A')
     AND NVL(ES_ANALISTA_SOPORTE, 0) = 1;

  IF v_analista = 0 THEN
    p_Message := 'El responsable seleccionado no esta marcado como analista de soporte.';
    RETURN;
  END IF;

  SELECT USUARIO_RESPONSABLE_ID INTO v_old
    FROM SOP_TICKET
   WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  UPDATE SOP_TICKET
     SET USUARIO_RESPONSABLE_ID = p_USUARIO_RESPONSABLE_ID,
         FECHA_ASIGNACION = NVL(FECHA_ASIGNACION, SYSDATE),
         UPDATED_BY = p_UPDATED_BY,
         UPDATED_AT = SYSDATE
   WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  INSERT INTO SOP_TICKET_HISTORIAL
    (HISTORIAL_ID, CODIGO_EMPRESA, TICKET_ID, TIPO_CAMBIO, CAMPO, VALOR_ANTERIOR, VALOR_NUEVO, COMENTARIO, USUARIO_ID)
  VALUES
    (SEQ_SOP_HIST.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, 'ASIGNACION', 'USUARIO_RESPONSABLE_ID', TO_CHAR(v_old), TO_CHAR(p_USUARIO_RESPONSABLE_ID), p_COMENTARIO, p_UPDATED_BY);

  INSERT INTO SOP_TICKET_NOTIF
    (NOTIF_ID, CODIGO_EMPRESA, TICKET_ID, USUARIO_DESTINO_ID, TIPO_DESTINO, EVENTO, TITULO, MENSAJE, CANAL)
  VALUES
    (SEQ_SOP_NOTIF.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, p_USUARIO_RESPONSABLE_ID, 'RESPONSABLE', 'ASIGNADO', 'Ticket asignado', 'Se le asigno un ticket de soporte.', 'INTERNA');

  INSERT INTO SIS_EMAIL_QUEUE (
    EMAIL_ID, CODIGO_EMPRESA, MODULO_ORIGEN, REFERENCIA_ID, TO_EMAIL,
    TO_NAME, SUBJECT, BODY_HTML, BODY_TEXT, FECHA_PROGRAMADA
  )
  SELECT SEQ_EMAIL_Q.NEXTVAL, p_CODIGO_EMPRESA, 'SOPORTE', p_TICKET_ID,
         u.EMAIL, u.USUARIO, 'Ticket asignado',
         '<p>Se le asigno un ticket de soporte.</p>',
         'Se le asigno un ticket de soporte.',
         SYSDATE
    FROM SIS_USUARIOS u
   WHERE u.CODIGO_USUARIO = p_USUARIO_RESPONSABLE_ID
     AND u.CODIGO_EMPRESA = p_CODIGO_EMPRESA
     AND NVL(u.RECIBE_EMAIL, 1) = 1
     AND u.EMAIL IS NOT NULL;

  p_Message := 'Success';
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    p_Message := 'No se encontró el ticket indicado.';
  WHEN OTHERS THEN
    p_Message := SQLERRM;
END;
