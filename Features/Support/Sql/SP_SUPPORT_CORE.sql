CREATE OR REPLACE PROCEDURE SP_SOP_TKT_STATUS_UPD (
  p_TICKET_ID IN NUMBER,
  p_ESTADO_ID IN NUMBER,
  p_UPDATED_BY IN NUMBER,
  p_COMENTARIO IN VARCHAR2,
  p_CODIGO_EMPRESA IN NUMBER,
  p_Message OUT VARCHAR2
) AS
  v_old NUMBER;
  v_solicitante NUMBER;
  v_responsable NUMBER;
  v_estado_nombre VARCHAR2(80);
  v_evento VARCHAR2(30);
  v_titulo VARCHAR2(200);
BEGIN
  SELECT t.ESTADO_ID, t.USUARIO_SOLICITANTE_ID, t.USUARIO_RESPONSABLE_ID,
         UPPER(NVL(e.NOMBRE, ''))
    INTO v_old, v_solicitante, v_responsable, v_estado_nombre
    FROM SOP_TICKET t
    LEFT JOIN SOP_ESTADO e ON e.ESTADO_ID = p_ESTADO_ID AND e.CODIGO_EMPRESA = p_CODIGO_EMPRESA
   WHERE t.TICKET_ID = p_TICKET_ID AND t.CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  IF v_estado_nombre LIKE '%RESUELT%' THEN
    v_evento := 'RESUELTO';
    v_titulo := 'Ticket resuelto';
  ELSIF v_estado_nombre LIKE '%REABIERT%' THEN
    v_evento := 'REABIERTO';
    v_titulo := 'Ticket reabierto';
  ELSIF v_estado_nombre LIKE '%CANCEL%' THEN
    v_evento := 'CANCELADO';
    v_titulo := 'Ticket cancelado';
  ELSE
    v_evento := 'ESTADO';
    v_titulo := 'Estado actualizado';
  END IF;

  IF v_evento = 'RESUELTO' THEN
    UPDATE SOP_TICKET
       SET FECHA_RESOLUCION = NVL(FECHA_RESOLUCION, SYSDATE)
     WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;
  END IF;

  SELECT ESTADO_ID INTO v_old
    FROM SOP_TICKET
   WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  UPDATE SOP_TICKET
     SET ESTADO_ID = p_ESTADO_ID,
         UPDATED_BY = p_UPDATED_BY,
         UPDATED_AT = SYSDATE
   WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  INSERT INTO SOP_TICKET_HISTORIAL
    (HISTORIAL_ID, CODIGO_EMPRESA, TICKET_ID, TIPO_CAMBIO, CAMPO, VALOR_ANTERIOR, VALOR_NUEVO, COMENTARIO, USUARIO_ID)
  VALUES
    (SEQ_SOP_HIST.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, 'ESTADO', 'ESTADO_ID', TO_CHAR(v_old), TO_CHAR(p_ESTADO_ID), p_COMENTARIO, p_UPDATED_BY);

  INSERT INTO SOP_TICKET_NOTIF
    (NOTIF_ID, CODIGO_EMPRESA, TICKET_ID, USUARIO_DESTINO_ID, TIPO_DESTINO, EVENTO, TITULO, MENSAJE, CANAL)
  SELECT SEQ_SOP_NOTIF.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, destino.USUARIO_ID,
         destino.TIPO_DESTINO, v_evento, v_titulo,
         'El estado del ticket fue actualizado.', 'INTERNA'
    FROM (
      SELECT v_solicitante USUARIO_ID, 'SOLICITANTE' TIPO_DESTINO FROM DUAL
      UNION
      SELECT v_responsable USUARIO_ID, 'RESPONSABLE' TIPO_DESTINO FROM DUAL
    ) destino
   WHERE destino.USUARIO_ID IS NOT NULL
     AND destino.USUARIO_ID <> p_UPDATED_BY;

  INSERT INTO SIS_EMAIL_QUEUE (
    EMAIL_ID, CODIGO_EMPRESA, MODULO_ORIGEN, REFERENCIA_ID, TO_EMAIL,
    TO_NAME, SUBJECT, BODY_HTML, BODY_TEXT, FECHA_PROGRAMADA
  )
  SELECT SEQ_EMAIL_Q.NEXTVAL, p_CODIGO_EMPRESA, 'SOPORTE', p_TICKET_ID,
         u.EMAIL, u.USUARIO, v_titulo,
         '<p>El estado del ticket fue actualizado.</p>',
         'El estado del ticket fue actualizado.',
         SYSDATE
    FROM SIS_USUARIOS u
   WHERE u.CODIGO_EMPRESA = p_CODIGO_EMPRESA
     AND u.CODIGO_USUARIO IN (v_solicitante, v_responsable)
     AND u.CODIGO_USUARIO <> p_UPDATED_BY
     AND NVL(u.RECIBE_EMAIL, 1) = 1
     AND u.EMAIL IS NOT NULL;

  p_Message := 'Success';
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    p_Message := 'No se encontró el ticket indicado.';
  WHEN OTHERS THEN
    p_Message := SQLERRM;
END;
/

CREATE OR REPLACE PROCEDURE SP_SOP_TKT_CLOSE (
  p_TICKET_ID IN NUMBER,
  p_UPDATED_BY IN NUMBER,
  p_OBSERVACION_RESOLUCION IN VARCHAR2,
  p_CODIGO_EMPRESA IN NUMBER,
  p_Message OUT VARCHAR2
) AS
  v_estado NUMBER;
  v_solicitante NUMBER;
  v_responsable NUMBER;
BEGIN
  SELECT ESTADO_ID INTO v_estado
    FROM SOP_ESTADO
   WHERE CODIGO_EMPRESA = p_CODIGO_EMPRESA
     AND UPPER(NOMBRE) = 'CERRADO'
     AND ROWNUM = 1;

  SELECT USUARIO_SOLICITANTE_ID, USUARIO_RESPONSABLE_ID
    INTO v_solicitante, v_responsable
    FROM SOP_TICKET
   WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  UPDATE SOP_TICKET
     SET ESTADO_ID = v_estado,
         FECHA_CIERRE = SYSDATE,
         FECHA_RESOLUCION = NVL(FECHA_RESOLUCION, SYSDATE),
         OBSERVACION_RESOLUCION = p_OBSERVACION_RESOLUCION,
         UPDATED_BY = p_UPDATED_BY,
         UPDATED_AT = SYSDATE
   WHERE TICKET_ID = p_TICKET_ID AND CODIGO_EMPRESA = p_CODIGO_EMPRESA;

  INSERT INTO SOP_TICKET_HISTORIAL
    (HISTORIAL_ID, CODIGO_EMPRESA, TICKET_ID, TIPO_CAMBIO, CAMPO, VALOR_NUEVO, COMENTARIO, USUARIO_ID)
  VALUES
    (SEQ_SOP_HIST.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, 'CIERRE', 'ESTADO_ID', TO_CHAR(v_estado), p_OBSERVACION_RESOLUCION, p_UPDATED_BY);

  INSERT INTO SOP_TICKET_NOTIF
    (NOTIF_ID, CODIGO_EMPRESA, TICKET_ID, USUARIO_DESTINO_ID, TIPO_DESTINO, EVENTO, TITULO, MENSAJE, CANAL)
  SELECT SEQ_SOP_NOTIF.NEXTVAL, p_CODIGO_EMPRESA, p_TICKET_ID, destino.USUARIO_ID,
         destino.TIPO_DESTINO, 'CERRADO', 'Ticket cerrado',
         'El ticket fue cerrado.', 'INTERNA'
    FROM (
      SELECT v_solicitante USUARIO_ID, 'SOLICITANTE' TIPO_DESTINO FROM DUAL
      UNION
      SELECT v_responsable USUARIO_ID, 'RESPONSABLE' TIPO_DESTINO FROM DUAL
    ) destino
   WHERE destino.USUARIO_ID IS NOT NULL
