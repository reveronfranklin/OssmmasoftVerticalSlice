CREATE OR REPLACE PROCEDURE SP_SOP_SLA_NOTIF (
  p_CODIGO_EMPRESA IN NUMBER,
  p_NOTIF_COUNT_OUT OUT NUMBER,
  p_Message OUT VARCHAR2
) AS
BEGIN
  INSERT INTO SOP_TICKET_NOTIF
    (NOTIF_ID, CODIGO_EMPRESA, TICKET_ID, USUARIO_DESTINO_ID, TIPO_DESTINO, EVENTO, TITULO, MENSAJE, CANAL)
  SELECT SEQ_SOP_NOTIF.NEXTVAL, t.CODIGO_EMPRESA, t.TICKET_ID, destino.USUARIO_ID,
         destino.TIPO_DESTINO, 'VENCIDO', 'Ticket vencido',
         'El ticket ' || t.TICKET_NUMERO || ' vencio por SLA.', 'INTERNA'
    FROM SOP_TICKET t
    JOIN SOP_ESTADO e ON e.ESTADO_ID = t.ESTADO_ID
    JOIN (
      SELECT TICKET_ID, USUARIO_SOLICITANTE_ID USUARIO_ID, 'SOLICITANTE' TIPO_DESTINO
        FROM SOP_TICKET
       WHERE CODIGO_EMPRESA = p_CODIGO_EMPRESA
      UNION
      SELECT TICKET_ID, USUARIO_RESPONSABLE_ID USUARIO_ID, 'RESPONSABLE' TIPO_DESTINO
        FROM SOP_TICKET
       WHERE CODIGO_EMPRESA = p_CODIGO_EMPRESA
    ) destino ON destino.TICKET_ID = t.TICKET_ID
   WHERE t.CODIGO_EMPRESA = p_CODIGO_EMPRESA
     AND destino.USUARIO_ID IS NOT NULL
     AND NVL(e.ES_FINAL, 0) = 0
     AND t.FECHA_VENCIMIENTO_SLA IS NOT NULL
     AND t.FECHA_VENCIMIENTO_SLA < SYSDATE
     AND NOT EXISTS (
       SELECT 1
         FROM SOP_TICKET_NOTIF n
        WHERE n.CODIGO_EMPRESA = t.CODIGO_EMPRESA
          AND n.TICKET_ID = t.TICKET_ID
          AND n.USUARIO_DESTINO_ID = destino.USUARIO_ID
          AND n.EVENTO = 'VENCIDO'
     );

  p_NOTIF_COUNT_OUT := SQL%ROWCOUNT;

  INSERT INTO SIS_EMAIL_QUEUE (
    EMAIL_ID, CODIGO_EMPRESA, MODULO_ORIGEN, REFERENCIA_ID, TO_EMAIL,
    TO_NAME, SUBJECT, BODY_HTML, BODY_TEXT, FECHA_PROGRAMADA
  )
  SELECT SEQ_EMAIL_Q.NEXTVAL, t.CODIGO_EMPRESA, 'SOPORTE', t.TICKET_ID,
         u.EMAIL, u.USUARIO, 'Ticket vencido por SLA',
         '<p>El ticket ' || t.TICKET_NUMERO || ' vencio por SLA.</p>',
         'El ticket ' || t.TICKET_NUMERO || ' vencio por SLA.',
         SYSDATE
    FROM SOP_TICKET t
    JOIN SOP_ESTADO e ON e.ESTADO_ID = t.ESTADO_ID
    JOIN SIS_USUARIOS u ON u.CODIGO_EMPRESA = t.CODIGO_EMPRESA
   WHERE t.CODIGO_EMPRESA = p_CODIGO_EMPRESA
     AND u.CODIGO_USUARIO IN (t.USUARIO_SOLICITANTE_ID, t.USUARIO_RESPONSABLE_ID)
     AND NVL(e.ES_FINAL, 0) = 0
     AND t.FECHA_VENCIMIENTO_SLA IS NOT NULL
     AND t.FECHA_VENCIMIENTO_SLA < SYSDATE
     AND NVL(u.RECIBE_EMAIL, 1) = 1
     AND u.EMAIL IS NOT NULL
     AND NOT EXISTS (
       SELECT 1
         FROM SIS_EMAIL_QUEUE q
        WHERE q.CODIGO_EMPRESA = t.CODIGO_EMPRESA
          AND q.MODULO_ORIGEN = 'SOPORTE'
          AND q.REFERENCIA_ID = t.TICKET_ID
          AND q.TO_EMAIL = u.EMAIL
          AND q.SUBJECT = 'Ticket vencido por SLA'
     );

  p_Message := 'Success';
EXCEPTION
  WHEN OTHERS THEN
    p_NOTIF_COUNT_OUT := 0;
    p_Message := SQLERRM;
END;
