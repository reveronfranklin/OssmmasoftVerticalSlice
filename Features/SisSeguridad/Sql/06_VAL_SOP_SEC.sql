PROMPT Usuarios con roles Soporte normalizados
SELECT ur.CODIGO_USUARIO,
       u.LOGIN,
       r.CLAVE,
       ur.CODIGO_EMPRESA,
       ur.ACTIVO
  FROM OSS_USR_ROL ur
  JOIN OSS_ROL r ON r.CODIGO_ROL = ur.CODIGO_ROL
  LEFT JOIN SIS_USUARIOS u ON u.CODIGO_USUARIO = ur.CODIGO_USUARIO
 WHERE r.CLAVE LIKE 'SOPORTE_%'
 ORDER BY ur.CODIGO_USUARIO, r.CLAVE;

PROMPT Usuarios con cache JSON_MENU que contiene Soporte
SELECT r.CODIGO_USUARIO_ROL,
       r.CODIGO_USUARIO,
       r.USUARIO,
       r.DESCRIPCION
  FROM OSS_USUARIO_ROL r
 WHERE DBMS_LOB.INSTR(r.JSON_MENU, '/apps/soporte/') > 0
    OR DBMS_LOB.INSTR(r.JSON_MENU, 'soporte.') > 0
 ORDER BY r.CODIGO_USUARIO;

